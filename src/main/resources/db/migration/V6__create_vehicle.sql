CREATE TABLE vehicle
(
    id                             INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    brand                          VARCHAR(255) NOT NULL,
    model                          VARCHAR(255) NOT NULL,
    production_year                DATE         NOT NULL,
    seats_amount                   INTEGER      NOT NULL,
    color                          VARCHAR(255) NOT NULL,
    rims_inch                      INTEGER      NOT NULL,
    capacity                       INTEGER      NOT NULL,
    engine_type                    VARCHAR(255) NOT NULL,
    power                          INTEGER      NOT NULL,
    torque                         INTEGER      NOT NULL,
    avg_fuel_consumption           REAL         NOT NULL,
    transmission                   VARCHAR(255) NOT NULL,
    ac                             BOOLEAN      NOT NULL,
    front_pdc                      BOOLEAN      NOT NULL,
    rear_pdc                       BOOLEAN      NOT NULL,
    bluetooth                      BOOLEAN      NOT NULL,
    led_front_lights               BOOLEAN      NOT NULL,
    xenon_front_lights             BOOLEAN      NOT NULL,
    led_rear_lights                BOOLEAN      NOT NULL,
    leather_seats                  BOOLEAN      NOT NULL,
    multifunctional_steering_wheel BOOLEAN      NOT NULL,
    cruise_control                 BOOLEAN      NOT NULL,
    category                       VARCHAR(1)   NOT NULL,
    basic_price                    DECIMAL      NOT NULL,
    photo_names                    VARCHAR(255) NOT NULL
);